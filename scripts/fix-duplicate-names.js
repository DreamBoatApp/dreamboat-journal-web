let fixed = 0;
let notFound = 0;

for (const [slug, newName] of Object.entries(FIXES)) {
    const filePath = path.join(dir, `${slug}.json`);
    if (!fs.existsSync(filePath)) {
        notFound++;
        continue;
    }

    try {
        const content = JSON.parse(fs.readFileSync(filePath, 'utf8'));
        const oldName = content.localizedName;
        content.localizedName = newName;
        // Update title too
        content.title = `Rüyada ${newName} Görmek: Anlamı ve Yorumu`;
        fs.writeFileSync(filePath, JSON.stringify(content, null, 2));
        fixed++;
    } catch (e) {
        console.error(`Error fixing ${slug}: ${e.message}`);
    }
}

// Verify remaining duplicates
const map = {};
fs.readdirSync(dir).filter(f => f.endsWith('.json')).forEach(f => {
    const c = JSON.parse(fs.readFileSync(path.join(dir, f)));
    const n = c.localizedName || f.replace('.json', '');
    if (!map[n]) map[n] = [];
    map[n].push(f.replace('.json', ''));
});
const remaining = Object.entries(map).filter(([k, v]) => v.length > 1);

console.log(`Fixed: ${fixed} slugs`);
console.log(`Not found: ${notFound} slugs`);
console.log(`Remaining duplicates: ${remaining.length} groups`);
if (remaining.length > 0) {
    remaining.forEach(([name, slugs]) => console.log(`  ${name}: ${slugs.join(', ')}`));
}
